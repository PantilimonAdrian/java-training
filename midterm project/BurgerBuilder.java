public abstract class BurgerBuilder {

    public abstract void addBread(String bread);

    public abstract void addPatty(String patty);

    public abstract void addVeggies(String veggies);

    public abstract void addSauce(String sauce);

    public abstract void addCheese(String cheese);

    public abstract void addBacon();

    public abstract void addExtraBacon();

    public abstract void addExtraCheese();
}
